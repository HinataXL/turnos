    private void actualizarEstadoBotones() {
        
        boolean sinMarcaje = (marcajeActual == null);
        
        btnEntrada.setEnabled(sinMarcaje);
        
        btnDescansoUno.setEnabled(!sinMarcaje && marcajeActual.getHoraDescanso1() ==  null);
        
        btnDescansoDos.setEnabled(!sinMarcaje && marcajeActual.getHoraDescanso1() != null && marcajeActual.getHoraDescanso2() == null);
        
        btnSalida.setEnabled(!sinMarcaje && marcajeActual.getHoraDescanso2() != null && marcajeActual.getHoraSalida() == null);
    }
        
    

