    private void iniciarReloj() {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("hh:mm:ss a");
        timer = new Timer(1000, e -> {
            lblTimer.setText(LocalTime.now().format(formatter));
            
        });
        timer.start();
    }
    
