    private void btnEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntradaActionPerformed
        this.marcajeActual = new Marcaje(this.usuarioActual);
        this.marcajeActual.setHoraEntrada(LocalTime.now());
       
        if (marcajeActual.getHoraEntrada().isAfter(LocalTime.of(8, 0))) {
            JOptionPane.showMessageDialog(this, "Entrada tarde registrada", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Entrada registrada: " + marcajeActual.getHoraEntrada());
        }
        
        actualizarEstadoBotones(); // Actualizamos la UI
    }//GEN-LAST:event_btnEntradaActionPerformed

    private void btnDescansoUnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescansoUnoActionPerformed
        if (marcajeActual == null) {
            JOptionPane.showMessageDialog(this, "Debe marcar la entrada primero", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        marcajeActual.setHoraDescanso1(LocalTime.now()); // <-- CORRECCIÓN CRÍTICA: Guardar la hora
        JOptionPane.showMessageDialog(this, "Descanso 1 registrado: " + marcajeActual.getHoraDescanso1(), "Éxito", JOptionPane.INFORMATION_MESSAGE);
        
        actualizarEstadoBotones(); // Actualizamos la UI
    }//GEN-LAST:event_btnDescansoUnoActionPerformed

    private void btnDescansoDosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescansoDosActionPerformed
            if (marcajeActual == null || marcajeActual.getHoraDescanso1() == null) {
            JOptionPane.showMessageDialog(this, "Debe marcar el primer descanso primero", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        marcajeActual.setHoraDescanso2(LocalTime.now()); // <-- CORRECCIÓN CRÍTICA: Guardar la hora
        JOptionPane.showMessageDialog(this, "Descanso 2 registrado: " + marcajeActual.getHoraDescanso2(), "Éxito", JOptionPane.INFORMATION_MESSAGE);
        
        actualizarEstadoBotones(); // Actualizamos la UI
    }//GEN-LAST:event_btnDescansoDosActionPerformed

    private void btnSalidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalidaActionPerformed
        if (marcajeActual == null || marcajeActual.getHoraDescanso2() == null) {
            JOptionPane.showMessageDialog(this, "Debe marcar ambos descansos primero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        marcajeActual.setHoraSalida(LocalTime.now());

        try {
            marcajeDAO.guardar(marcajeActual);
            JOptionPane.showMessageDialog(this, "Salida registrada: " + marcajeActual.getHoraSalida() + "\nEl marcaje completo ha sido guardado.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            
            // Reinicia todo para el siguiente marcaje
            marcajeActual = null;
            actualizarEstadoBotones(); // Actualizamos la UI al estado inicial

        } catch (java.io.IOException e) {
            JOptionPane.showMessageDialog(this, "Error crítico al guardar el archivo: " + e.getMessage(), "Error de Guardado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalidaActionPerformed

    private void btnRegresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegresarActionPerformed
        if (timer != null) {
            timer.stop();
        }
        this.dispose();
        // Asumiendo que tu clase de menú se llama Menu_Principal
        PanelDeEmpleado panel = new PanelDeEmpleado(usuarioActual);
        panel.setVisible(true);
    }//GEN-LAST:event_btnRegresarActionPerformed

