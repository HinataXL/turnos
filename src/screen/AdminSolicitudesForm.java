   private void cargarSolicitudesPendientes() {
    // Obtiene el modelo de la tabla (asumiendo que se llama jTable1)
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
    // Limpia la tabla por si tenía datos viejos
    model.setRowCount(0);

    // Llama al DAO
    SolicitudCambioDAO dao = new SolicitudCambioDAO();
    List<SolicitudCambio> solicitudes = dao.listarTodas();
    
    // Llena la tabla solo con las solicitudes PENDIENTES
    for (SolicitudCambio s : solicitudes) {
        if ("Pendiente".equalsIgnoreCase(s.getEstado())) {
            model.addRow(new Object[]{
                s.getUsuarioEmpleado(),
                s.getFechaInicial(),
                s.getFechaNueva(),
                s.getJustificacion()
                // No mostramos el estado porque ya sabemos que es "Pendiente"
            });
        }
    }
}
